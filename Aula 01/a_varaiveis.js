// O que são variáveis
/*
    Variáveis em Javascript são formas de salvar dados que possam ser acessados e/ou manipulados posteriormente, 
    independente do tipo desses dados, strings, números, booleanos, arrays, objetos ou funções.
*/


// Tipo de Variáveis em JS
// Var Let Const

// EXEMPLOS
var nome1 = 'Vinicio Deivid Brejinski'
let idade = 24
const dataNascimengto = '27/01/1997'

// Diferença entre Var Let & Const

// Var - Declara uma variável, opcionalmente, inicializando-a com um valor.
// Var foi a primeira Forma de declarar variavel e era a única até um tempo
// Var pode ser Redeclarada quantas vezes vc quiser

// Let - Declara uma variável local de escopo do bloco, opcionalmente, inicializando-a com um valor.
// Let é forma mais recente e mais usada para declarar variáveis 
// Let tem algumas diferenças da var, a let não pode ser redeclarada e ela respeita o scopo em que ela foi criada

// Const - Declara uma constante de escopo de bloco, apenas de leitura.
// Const é uma "Variável" que não muda, o seu valor não se altera depois de criado e não se redeclara const



